void Map::load_map(int arr[20][25]) {
	for (int row = 0; row < 20; row++) {
		for (int col = 0; col < 25; col++) {
			map[row][col] = arr[row][col];
		}
	}

	src.x = src.y = 0;
	src.w = src.h = 32;
	destination.w = destination.h = 32;
	destination.x = 0;
	destination.y = 0;

}

void Map::draw_map() {
	int type = 0;

	for (int row = 0; row < 20; row++) {
		for (int col = 0; col < 25; col++) {
			type = map[row][col];

			destination.x = col * 32;
			destination.y = row * 32;

			switch (type) {
			case 0:
				texture_manager::draw(water, src, destination);
				break;
			case 1:
				texture_manager::draw(grass, src, destination);
				break;
			case 2:
				texture_manager::draw(dirt, src, destination);
				break;
			}
		}
	}

}